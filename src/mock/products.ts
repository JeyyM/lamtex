export function getProductById(id: string): Product | undefined {
  return products.find(p => p.id === id);
}

export function getProductsByCategory(category: string): Product[] {
  if (category === 'All') return products;
  return products.filter(p => p.category === category);
}

export function getVariantsByProductId(productId: string): ProductVariant[] {
  return productVariants.filter(v => v.productId === productId);
}

export function getVariantById(id: string): ProductVariant | undefined {
  return productVariants.find(v => v.id === id);
}

export function getProductPerformance(productId: string): ProductPerformance[] {
  return productPerformance.filter(p => p.productId === productId);
}

