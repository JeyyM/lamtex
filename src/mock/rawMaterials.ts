export const getAllRawMaterials = () => rawMaterials;

export const getRawMaterialById = (id: string) => {
  return rawMaterials.find(m => m.id === id);
};

export const getRawMaterialsByCategory = (category: string) => {
  if (category === 'All') return rawMaterials;
  return rawMaterials.filter(m => m.category === category);
};

export const getLowStockMaterials = () => {
  return rawMaterials.filter(m => m.totalStock <= m.reorderPoint);
};

export const getMaterialsByStatus = (status: string) => {
  if (status === 'All') return rawMaterials;
  return rawMaterials.filter(m => m.status === status);
};

export const getBatchesByMaterialId = (materialId: string) => {
  return materialBatches.filter(b => b.materialId === materialId);
};

export const getStockMovementsByMaterialId = (materialId: string) => {
  return stockMovements.filter(m => m.materialId === materialId);
};

export const getPendingPurchaseRequisitions = () => {
  return purchaseRequisitions.filter(pr => pr.status === 'Pending Approval');
};

export const getPurchaseRequisitionsByStatus = (status: string) => {
  if (status === 'All') return purchaseRequisitions;
  return purchaseRequisitions.filter(pr => pr.status === status);
};

export const getPurchaseOrdersByStatus = (status: string) => {
  if (status === 'All') return purchaseOrders;
  return purchaseOrders.filter(po => po.status === status);
};

export const getSupplierById = (id: string) => {
  return suppliers.find(s => s.id === id);
};

export const getAllSuppliers = () => suppliers;

export const getTotalInventoryValue = () => {
  return rawMaterials.reduce((sum, m) => sum + m.totalValue, 0);
};

export const getMaterialsRequiringReorder = () => {
  return rawMaterials.filter(m => m.totalStock <= m.reorderPoint);
};
